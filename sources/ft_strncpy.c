#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	if (!dst || !src)
		return (NULL);
	i = 0;
	while (src[i] && i < len)
	{
		dst[i] = src[i];
		i++;
	}
	while (!src[i] && len >= i + 1)
	{
		len--;
		dst[len] = 0;
	}
	return (dst);
}

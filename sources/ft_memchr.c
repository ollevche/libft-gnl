#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	if (!s)
		return (NULL);
	while (i < n && *(char*)(s + i) != (char)c)
		i++;
	if (i < n)
		return ((void*)(s + i));
	else
		return (NULL);
}
